/// Radio Buttons in Flutter
//
/// What are Radio Buttons?
//
// Small round buttons where you can pick only one choice from many.
//
// Once you pick one, you can't pick another unless you change it.
//
//
/// When to Use Radio Buttons?
//
// When you have a few choices (like 2 to 6 options).
//
// When you want to see all choices at once.
//
//
//
// ---


/// How They Work
/// Radio Buttons properties
//
// Group Value: Tracks your choice.
//
// Value: Each button has its own label.
//
// OnChanged: What happens when you pick a button.
//
// Label: The name next to the button that tells you what it means.
//
//
/// How They Work
/// Radio Buttons
//
// Always pick one option by default.
//
// Keep choices simple and easy to read.
//
//
//





/// Dropdowns in Flutter
//
/// What are Dropdowns?
//
// A button that shows a list of choices when you click it.
//
// After you pick one, the list goes away.
//
//
/// When to Use Dropdowns?
//
// When you have many choices.
//
// When you want a clean and tidy screen.
//
//
//
// ---
//
/// How They Work
//

/// Dropdowns   properties
//
// Selected Value: The choice you pick.
//
// Items List: All the options you can choose from.
//
// Hint Text: A message like "Choose one" before you pick.
//
// OnChanged: What happens when you pick something.
//
//
//
// ---
//
/// Best Practices
//

/// Dropdowns
//
// Show a default choice.
//
// Keep options short and easy to understand.
//
//
//
// ---
//
/// Summary
//
// Radio Buttons
//
// Good when you have a few choices and want to pick one.
//
// Shows all choices at once.
//
//
/// Dropdowns
//
// Good for many choices.
//
// Saves space and looks tidy.


